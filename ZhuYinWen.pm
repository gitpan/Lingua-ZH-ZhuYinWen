use String::Multibyte;

our %table;
foreach (grep {$_} split $/, $MAPPING){
    my @c = split /\s/, $_;
    $table{$c[0]} = $c[1];
}
our $encoding = 'Big5';
sub encoding { $encoding = $_[0] || $encoding }
sub bastardize {
    my $b = String::Multibyte->new($encoding) or die;
    my $s ='';
    foreach ($b->strsplit('',shift)){ $s .= $table{$_} ? $table{$_} : $_; }
    $s;
}

