=head1 NAME

Lingua::ZH::ZhuYinWen - ZhuYinWen Bastardizer

=head1 SYNOPSIS

  use Lingua::ZH::ZhuYinWen qw(bastardize);
  bastardize($text);

=head1 DESCRIPTION

In taiwan, traditionally Mandarin's phonetics is marked by a system called ZhuYinFuHao which is composed of 37 phonetic symbols. It is not used in formal language, and only helps children to learn Mandarin. Because of prevalence of internet and complex Chinese input methods which are frustrating to some people who are not handy at keyboard, more and more people are used to typing these phonetic symbols in place of standard Han characters. This phenomenon is called B<ZhuYinWen>. It leads to obfuscation, great reading difficulty, or even a darned mess.

This module converts Mandarin to ZhuYinWen. However, there is no one-to-one correspondence between morphology and phonology in Chinese. Moreover, full pronunciation is replaced with a single phoneme. Thus perfect two-way conversion is I<impossible>. This module only provides Mandarin-to-ZhuYinWen converting method.

